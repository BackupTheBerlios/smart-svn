/**
 * user_view_index class of the template "index.tpl.php"
 *
 */
 
class user_view_index
{
    /**
     * Global system instance
     * @var object $B
     */
    var $B;
    
    /**
     * constructor
     *
     */
    function user_view_index()
    {
        $this->__construct();
    }

    /**
     * constructor php5
     *
     */
    function __construct()
    {
        $this->B = & $GLOBALS['B'];
    }
    
    /**
     * Execute the view of the template "index.tpl.php"
     * create the template variables
     * and listen to an action
     *
     * @return bool true on success else false
     */
    function perform()
    {
        if(!isset($_REQUEST['sec']) || empty($_REQUEST['sec'])) 
        {
            $_REQUEST['sec'] = 'default';
        }
        return TRUE;
    }    
}

